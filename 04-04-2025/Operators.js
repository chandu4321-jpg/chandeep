let A = 10,
 B = 20;
 
 //Airthematic Operators//
 
console.log(A + B);  
 
console.log(B - A);
 
console.log(A * B);   
 
console.log(B / A);   
 
console.log(B % A);   
 
console.log(A ** 2); 
 
 
// Increment (Pre and Post)
console.log(++A);    
 
console.log(B++);     
 
console.log(B);       
 
 
// Decrement (Pre and Post)
console.log(--A);   
 
console.log(B--);     
 
console.log(B);   
 
console.log( --B + A--);
 
console.log(A++ + --B);
 
 
//Assignment Operators 
 
A += 5;
console.log(A);       
 
B -= 10;
console.log(B);       
 
A *= 2;
console.log(A);       
 
B /= 2;
console.log(B);       
 
 
//Comparison Operators
 
console.log(A < B);    
 
console.log(B > A);    
 
console.log(A <= 10);  
 
console.log(B >= 30);  
 
 
// Equality
 
console.log(A == "10");   
 
console.log(A === "10");  
 
 
// Inequality
 
console.log(A != "20");   
 
console.log(B !== "20");

 